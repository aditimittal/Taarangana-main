                                <p><p>Customer Analytics is about using data to build better products, to provide an excellent customer experience and to grow your business. It’s about making high quality decisions - knowing when to double down or knowing when it’s time to course-correct.</p>
<p>Whether you're a startup founder, an industry professional or a research academic, it is the key to finding the right customers, validating whether a problem is real, deciding what to build and how to monetize it. The Customer Analytics Dublin Meetup brings together industry and academic experts that focus their efforts on the analysis and understanding of customer behaviour on different channels (web, transactional, CRM, etc) in order to improve customer experience, retention and monetization.</p>
<p>At each event we host 2 to 3 key speakers to share their experience and knowledge on one or more of the following topics: 
<ul>
<li>Predictive customer analytics</li>
<li>Customer understanding, retention and churn </li>
<li>Web analytics </li>
<li>Big data & machine learning </li>
<li>Customer clustering & segmentation </li>
<li>Customer data monetisation and data leverage </li>
<li>Marketing & attribution models</li>
</ul>
</p>
<p>
The Customer Analytics Meetup take place in Dublin City Centre every 1 / 2 months. Join us to hear from some inspiring speakers, learn how Customer Analytics makes an impact and meet with like minded people. There will be some snacks, drinks and plenty of opportunity for networking.</p>

